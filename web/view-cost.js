// paintCostSkeletons fills the empty SHELL with shimmer placeholders.
// Each spot gets overwritten when real content arrives: hbar lists via
// innerHTML, tbodies via buildRows().
function paintCostSkeletons(container) {
  const barsModel = container.querySelector('#cost-bars-model');
  if (barsModel) barsModel.innerHTML = hbarListSkeleton(5);
  const barsProject = container.querySelector('#cost-bars-project');
  if (barsProject) barsProject.innerHTML = hbarListSkeleton(8);
  const tables = {
    model: 6, project: 6, skill: 5, prompt: 6,
  };
  for (const [name, cols] of Object.entries(tables)) {
    const tb = container.querySelector(`[data-table="${name}"] tbody`);
    if (tb) tb.innerHTML = tableBodySkeleton(cols, 6);
  }
}

function activateSubview(container, sub) {
  const subs = container.querySelectorAll('.subtab[data-subtab]');
  if (!subs.length) return;
  const wanted = sub && container.querySelector(`.subtab[data-subtab="${sub}"]`)
    ? sub
    : subs[0].dataset.subtab;
  subs.forEach(t => t.classList.toggle('is-active', t.dataset.subtab === wanted));
  container.querySelectorAll('.subview').forEach(s =>
    s.classList.toggle('is-active', s.dataset.subview === wanted));
}

function updateNavMetric(id, html) {
  const el = document.getElementById(id);
  if (!el) return;
  if (html.startsWith('<')) el.innerHTML = html;
  else el.textContent = html;
}

let painted = false;
let navPainted = false;

// paintNav fetches /cost just to derive the sidebar metric (top model
// by spend). Called from app.js at startup so the metric resolves
// before the user clicks the tab; the full paint() reuses the same
// endpoint via the server's render cache.
export async function paintNav() {
  if (navPainted || painted) return;
  let cost;
  try { cost = await fetchCost(); } catch { return; }
  const byModel = cost.by_model || [];
  const topModel = byModel.filter(r => r.CostUSD > 0)[0];
  updateNavMetric('nav-metric-cost', topModel
    ? `${truncate(topModel.Model, 18)} · ${fmtMoney(topModel.CostUSD)}`
    : '—');
  navPainted = true;
}

export async function paint(route) {
  const container = document.getElementById('view-cost');
  if (!container) return;

  if (painted) {
    activateSubview(container, route.sub);
    return;
  }

  container.innerHTML = SHELL;
  paintCostSkeletons(container);

  let cost;
  try {
    cost = await fetchCost();
  } catch (err) {
    container.innerHTML = `<header class="view-head"><h1>${labelIcon('cost')}Cost</h1></header>
      <div class="warning-card" role="alert"><strong class="danger">Failed to load cost data:</strong> ${escHtml(err.message)}</div>`;
    return;
  }

  const byModel = cost.by_model || [];
  const byProject = cost.by_project || [];
  const bySkill = cost.by_skill || [];
  const byPrompt = cost.by_prompt || [];
  // total_cost_usd ships from Go (aggregate.Totals().CostUSD) — read it
  // rather than re-summing per-row costs. The per-row pct() division
  // stays client-side (which denominator applies is a per-view choice).
  const totalCost = cost.total_cost_usd || 0;

  // SSR-equivalent hbar rendering. Static report SSRs these; the SPA
  // builds them client-side from the same data.
  const barsModel = container.querySelector('#cost-bars-model');
  if (barsModel) barsModel.innerHTML = modelBarsHTML(byModel, totalCost);
  const barsProject = container.querySelector('#cost-bars-project');
  if (barsProject) barsProject.innerHTML = projectBarsHTML(byProject, totalCost);

  // Trend column injection + sparkline data. Period+trends arrive
  // alongside the data so we know whether to inject the Trend column
  // at all. Fetched in parallel so cost rendering isn't blocked when
  // --by is unset (the common case for serve mode).
  let modelTrends = null, projectTrends = null, period = '';
  try {
    const [m, p] = await Promise.all([
      fetchTrends('model').catch(() => null),
      fetchTrends('project').catch(() => null),
    ]);
    modelTrends = m;
    projectTrends = p;
    period = (m && m.period) || (p && p.period) || '';
  } catch { /* no-op — trend column stays uninjected */ }

  const modelTable = container.querySelector('[data-table="model"]');
  const projectTable = container.querySelector('[data-table="project"]');
  if (period) {
    injectTrendColumn(modelTable, 'OutputTokens', period);
    injectTrendColumn(projectTable, 'DominantModel', period);
  }

  buildRows(modelTable, byModel, r => withTrend([
    [escHtml(r.Model), false],
    [escHtml(fmtMoney(r.CostUSD)), true, r.CostUSD],
    [pct(r.CostUSD, totalCost), true],
    [fmtNum(r.Turns), true],
    [fmtNum(r.OutputTokens), true],
    [fmtNum(r.CacheReadTokens), true],
  ], 4, modelTrends && modelTrends.series ? modelTrends.series[r.Model] : null, period, trendSpark));

  buildRows(projectTable, byProject, r => withTrend([
    [`<span class="truncate path" title="${escHtml(r.Project)}">${escHtml(r.Project)}</span>`, false],
    [escHtml(fmtMoney(r.CostUSD)), true, r.CostUSD],
    [pct(r.CostUSD, totalCost), true],
    [fmtNum(r.Sessions), true],
    [fmtNum(r.Turns), true],
    [escHtml(r.DominantModel), false],
  ], 5, projectTrends && projectTrends.series ? projectTrends.series[r.Project] : null, period, trendSpark));

  buildRows(container.querySelector('[data-table="skill"]'), bySkill, r => [
    [`<code>${escHtml(r.Key)}</code>`, false],
    [fmtNum(r.Count), true],
    [fmtNum(r.TurnCount), true],
    [escHtml(fmtMoney(r.CostUSD)), true, r.CostUSD],
    [fmtNum(r.OutputTokens), true],
  ]);

  buildRows(container.querySelector('[data-table="prompt"]'), byPrompt, r => {
    const sample = r.Sample || r.Key || '';
    const head = sample.length > 80 ? sample.slice(0, 79) + '…' : sample;
    return [
      [`<span title="${escHtml(sample)}">${escHtml(head)}</span>`, false],
      [fmtNum(r.Invocations), true],
      [fmtNum(r.Sessions), true],
      [fmtNum(r.TurnCount), true],
      [escHtml(fmtMoney(r.CostUSD)), true, r.CostUSD],
      [pct(r.CostUSD, totalCost), true],
    ];
  });

  wireGlobalFilters();
  activateSubview(container, route.sub);

  // Sidebar metric — top model by cost. Mirrors the legacy navMetrics().
  const topModel = byModel.filter(r => r.CostUSD > 0)[0];
  updateNavMetric('nav-metric-cost', topModel
    ? `${truncate(topModel.Model, 18)} · ${fmtMoney(topModel.CostUSD)}`
    : '—');

  painted = true;
  navPainted = true;
}

export function reset() { painted = false; navPainted = false; }
