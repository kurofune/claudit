const labelIcon = id => `<svg class="icon" aria-hidden="true"><use href="#icon-${id}"/></svg>`;

function hbarRowHTML(label, cost, totalCost, extra, truncateLabel) {
  const pctW = totalCost > 0 ? (100 * cost / totalCost).toFixed(1) : '0.0';
  const visible = truncateLabel ? truncate(label, 70) : label;
  const extraVal = extra
    ? extra
    : ` (${totalCost > 0 ? (100 * cost / totalCost).toFixed(1) + '%' : '—'})`;
  return `<div class="hbar">
    <div class="fill" style="width:${pctW}%"></div>
    <div class="lbl" title="${escHtml(label)}">${escHtml(visible)}</div>
    <div class="val">${escHtml(fmtMoney(cost))}${escHtml(extraVal)}</div>
  </div>`;
}

function modelBarsHTML(rows, totalCost) {
  const priced = rows.filter(r => r.CostUSD > 0);
  if (priced.length === 0) {
    return `<p class="small empty-state">No priced model data. Add prices for any unknown models in <code>~/.config/claudit/prices.yaml</code>.</p>`;
  }
  return priced.map(r => hbarRowHTML(r.Model, r.CostUSD, totalCost, '', false)).join('');
}

function projectBarsHTML(rows, totalCost) {
  return rows.slice(0, 20)
    .map(r => hbarRowHTML(r.Project, r.CostUSD, totalCost, '', true)).join('');
}

